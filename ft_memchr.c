#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char *p;

	p = s;
	if (n != 0)
		while (n-- != 0)
			if (*p++ == (char)c)
				return (void*)(p - 1);
	return (0);
}
